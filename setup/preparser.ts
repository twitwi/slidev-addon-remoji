import { definePreparserSetup } from "@slidev/types";

const fixer = (m, type) => {
  // all
  m['👓'] = 'glasses'
  m['🐺'] = 'wolf'
  m['🦓'] = 'zebra'
  m['🦊'] = 'fox'
  if (type === 'openmoji' || type === 'twemoji' || type === 'noto') {
    m['🐼'] = 'panda'
    m['💻'] = 'laptop'
    m['🤹'] = 'person-juggling'
    m['🐸'] = 'frog'
    m['🐹'] = 'hamster'
    m['🐻'] = 'bear'
    m['🦄'] = 'unicorn'
    m['🦁'] = 'lion'
    m['🦒'] = 'giraffe'
  }
  if (type === 'openmoji' || type === 'noto') {
    m['🧑'] = 'person'
  }
  if (type === 'emojione') {
    m['💻'] = 'laptop'
    m['🤹'] = 'person-juggling'
    delete m['🦒']
    delete m['🦥']
    delete m['🧑']
  }
}

export default definePreparserSetup(({headmatter}) => {
  const mapper = baseMap()
  const type = headmatter?.addonsConfig?.remoji ?? 'openmoji'
  fixer(mapper, type)
  return [
    {
      transformRawLines(lines) {
        var ranges = [
          "\ud83c[\udf00-\udfff]", // U+1F300 to U+1F3FF
          "\ud83d[\udc00-\udfff]", // U+1F400 to U+1F7FF
          "\ud83e[\udc00-\udfff]", // U+1F800 to U+1FAFF
          //OR JUST           '[\ud83c-\ud83e][\udc00-\udfff]', // U+1F300 to U+1FAFF
        ];
        var re = new RegExp(ranges.join("|"), "g");

        const VOID = '​' // Zero-Width Space, to avoid merging a paragraph that would start with an emoji
        for (const i in lines) {
          for (const k in mapper) {
            lines[i] = lines[i].replace(re, (m) => mapper[m] ? `${VOID}<${type}-${mapper[m]}/>` : m);
          }
        }
      },
    },
  ];
});

